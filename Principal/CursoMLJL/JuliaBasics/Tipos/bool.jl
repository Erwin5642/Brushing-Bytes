#Operadores de Comparação entre valores 
3 < 2 #false
3 > 1 #true
2 >= 1 #true 
3 <= 0 #false 
4 == 4 #true 
1.4 - 1.3 == 0.1 #false por erro de conversão 
2 != 1 #true

#Operadores booleanos
1 < 2 || 2 < 3 #or
2 == 3 && 1 < 2 #and
#Os operadores também poder ser escritos apenas como | ou &
#Além disso, booleanos também possuem valores inteiro. False = 0 e true = 1
false + true + true # = 2
